package com.example.lebsmart.Others;

public class AddBuilding {

    public String bname, nofs, lat, lon, totalApartments, occupiedApartments, endemicBuilding;

    public AddBuilding(String bname, String nofs, String lat, String lon,
                       String totalApartments, String occupiedApartments, String endemicBuilding) {
        this.bname = bname;
        this.nofs = nofs;
        this.lat = lat;
        this.lon = lon;
        this.totalApartments = totalApartments;
        this.occupiedApartments = occupiedApartments;
        this.endemicBuilding = endemicBuilding;
    }

}
