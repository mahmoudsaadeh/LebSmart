package com.example.lebsmart.MeetingsFragments;

public class MeetingAdd {

    public String meetingTimeAdd;
    public String meetingDateAdd;
    public String meetingPlaceAdd;
    public String meetingTitleAdd;
    public String meetingDescriptionAdd;

    public MeetingAdd(String meetingTimeAdd, String meetingDateAdd, String meetingPlaceAdd, String meetingTitleAdd, String meetingDescriptionAdd) {
        this.meetingTimeAdd = meetingTimeAdd;
        this.meetingDateAdd = meetingDateAdd;
        this.meetingPlaceAdd = meetingPlaceAdd;
        this.meetingTitleAdd = meetingTitleAdd;
        this.meetingDescriptionAdd = meetingDescriptionAdd;
    }



}
